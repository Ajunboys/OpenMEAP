package com.openmeap.protocol.dto;

public enum UpdateType {

    REQUIRED("required"),
    OPTIONAL("optional"),
    IMMEDIATE("immediate");
    private final String value;

    UpdateType(String v) {
        value = v;
    }

    public String value() {
        return value;
    }

    public static UpdateType fromValue(String v) {
        for (UpdateType c: UpdateType.values()) {
            if (c.value.equals(v)) {
                return c;
            }
        }
        throw new IllegalArgumentException(v);
    }

}
